#include <stdio.h>

int main()
{
    // program to display two individual integers
    
    int first, second;

    printf("Enter first integer : ");
    scanf(" %d", &first);

    printf("Enter second integer : ");
    scanf(" %d", &second);

    printf("\nFirst integer : %d\n", first);
    printf("Second integer : %d\n", second);
    return 0;
}